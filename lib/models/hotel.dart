
class Hotel {
  final String id;
  final String title;
  final String imageUrl;
  final String description;
  final double price;
  final double distance;
  final int likes;

  Hotel({
    required this.id,
    required this.title,
    required this.imageUrl,
    required this.description,
    required this.price,
    required this.distance,
    required this.likes,
  });
}